/*

1) npm install -g webpack          

2) opretter et directory

3) index.html og main.js

4) <script src="bundle.js"></script> i index.html

5) webpack main.js bundle.js

    - oprettelse af bundle.js

6) webpack.config.js

    - eksporter projektets webpack configurations object
    - nu kan man køre applikation ved kun at skrive 'webpack'

7) Watchmode

    - Når der sker ændringer, kan webpack overvåge projektet
    - se config file

8) Loaders

    - Webpack kender ikke til 'loaders'. Kan kun læse .js filer
    - HUSK FØRST npm init for package.json file
    - npm install

9) Babel transpiler, convert kode

    - npm install --save-dev babel-loader babel-core babel-preset-es2015
    - ses i config file

10) Development Server

    - npm install webpack-dev-server -g
    - run webpack-dev-server


*/